using System;
using System.Linq;
using System.Web.Http;
using System.Data.Entity;
using AutoMapper;
using MovieBase.Dtos;
using MovieBase.Models;

namespace MovieBase.Controllers.API {
    public class CustomersController : ApiController {
        // Used to access the database for this class.
        private ApplicationDbContext _context;

        public CustomersController() {
            // Initialized upon calling this controller during run-time.
            _context = new ApplicationDbContext();
        }

        // GET /api/customers
        public IHttpActionResult GetCustomers() {
            // Returns a list of objects (customers) & uses Mapper to utilize CustomerDto.
            var customerDto = _context.Customers.Include(c => c.MembershipType)
                .ToList().Select(Mapper.Map<Customer, CustomerDto>);
            // Then return the customer result from the Mapper method.
            return Ok(customerDto);
        }

        // GET /api/customers/1
        public IHttpActionResult GetCustomer(int id) {
            // Returns one object (customer) by using a matching id.
            Customer customer = _context.Customers.SingleOrDefault(c => c.Id == id);
            // If there's no customer, return the standard not found http response.
            if (customer == null) return NotFound();
            // Otherwise return the customer via the Mapper method.
            return Ok(Mapper.Map<Customer, CustomerDto>(customer));
        }

        // POST /api/customers
        [HttpPost]
        public IHttpActionResult CreateCustomer(CustomerDto customerDto) {
            // Changes the program's flow via using validation data or return an error.
            if (!ModelState.IsValid) return BadRequest();

            // Maps CustomerDto to the Customer class before touching the database.
            Customer customer = Mapper.Map<CustomerDto, Customer>(customerDto);
            // Adds it to dbContext before saving it to the database. Finally return customer.
            _context.Customers.Add(customer);
            _context.SaveChanges();

            // Copies over the id generated by the database for customer.
            customerDto.Id = customer.Id;
            // Creates the pathway to a specific customer via the id
            // generated and using customerDto that was just created.
            return Created(new Uri(Request.RequestUri + "/" + customer.Id), customerDto);
        }

        // PUT /api/customers/1
        [HttpPut]
        public IHttpActionResult UpdateCustomer(int id, CustomerDto customerDto) {
            // Changes the program's flow via using validation data or return an error.
            if (!ModelState.IsValid) return BadRequest();
            // Returns one object (customer) by using a matching id.
            Customer customerInDb = _context.Customers.SingleOrDefault(c => c.Id == id);
            // If there's no customer, return the standard not found http response.
            if (customerInDb == null) return NotFound();

            // Maps the customer Dto variant to the customer's database variant.
            Mapper.Map(customerDto, customerInDb);
            // Saves the changes to the database.
            _context.SaveChanges();
            return Ok();
        }

        // DELETE /api/customers/1
        [HttpDelete]
        public IHttpActionResult DeleteCustomer(int id) {
            // Returns one object (customer) by using a matching id.
            Customer customerInDb = _context.Customers.SingleOrDefault(c => c.Id == id);
            // If there's no customer, return the standard not found http response.
            if (customerInDb == null) return NotFound();

            // Delete the customer & saves the changes to the database.
            _context.Customers.Remove(customerInDb);
            _context.SaveChanges();
            return Ok();
        }
    }
}